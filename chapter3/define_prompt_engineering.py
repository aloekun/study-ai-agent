from openai import OpenAI

client = OpenAI()

response = client.chat.completions.create(
    model="gpt-4o-mini",
    messages=[
        {"role": "user", "content": "プロンプトエンジニアリングとは"},
    ],
)
print(response.choices[0].message.content)

# 出力例
# プロンプトエンジニアリングとは、主に人工知能（AI）や機械学習のモデル、特に自然言語処理（NLP）モデルに対して、効果的な入力（プロンプト）を設計・開発する技術や手法のことを指します。これにより、AIが生成するアウトプットの品質や関連性を向上させることが目的です。
# プロンプトエンジニアリングのプロセスには、以下のような要素が含まれます：
# 1. **プロンプトの設計**: モデルに対して与える質問やタスクをどのように構造化するかを考える。具体的には、文の構築、キーワードの選択、文脈の提供などが含まれます。
# 2. **テストと評価**: 作成したプロンプトを使用して、AIの応答を評価します。どのプロンプトが最も効果的か、またはどのように改善できるかを分析します。
# 3. **反復法**: プロンプトの改良を行うために、結果をもとに再度プロンプトを設計し、評価を行うサイクルを繰り返します。
# 4. **コンテキストの活用**: モデルに対して適切な文脈を与え、応答の質を向上させる方法を探ります。
# プロンプトエンジニアリングは、AIを使ったアプリケーションやサービスの精度や有用性を高めるためにますます重要になっています。特に、大規模な言語モデル（例えばGPT-3やGPT-4など）が普及している現代において、その技術は多様な分野で役立てられています。
